int	ft_atoi(char *str)
{
	int i;
	int sign;
	int nb;

	nb = 0;
	i = 0;
	sign = 1;
	while (((str[i] >= 8 && str[i] <= 14) || str[i] == 32) && str[i])
		i++;
	while ((str[i] == '+' || str[i] == '-') && str[i])
	{
		str[i] == '-' ? sign *= -1 : sign;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i])
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	return (nb * sign);
}
