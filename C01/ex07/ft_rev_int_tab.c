void	ft_rev_int_tab(int *tab, int size)
{
	int x;
	int y;
	int tmp_int;

	x = 0;
	y = size - 1;
	while (x <= y)
	{
		tmp_int = tab[x];
		tab[x] = tab[y];
		tab[y] = tmp_int;
		x++;
		y--;
	}
}
