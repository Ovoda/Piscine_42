int	add(int x, int y)
{
	return (x + y);
}

int	sub(int x, int y)
{
	return (x - y);
}

int	div(int x, int y)
{
	return (x / y);
}

int	mult(int x, int y)
{
	return (x * y);
}

int	mod(int x, int y)
{
	return (x % y);
}
